/**
 * Create a driver profile by reading DMI from a LINBO client via SSH.
 * @param {string} hostIp - IP of the LINBO client to read DMI from
 * @returns {Promise<{ created: boolean, folder: string, vendor: string, product: string }>}
 */
async function createProfile(hostIp) {
  // 1. SSH to host and read DMI data
  const result = await sshService.executeWithTimeout(
    hostIp,
    'cat /sys/class/dmi/id/sys_vendor && echo "---DMI_SEP---" && cat /sys/class/dmi/id/product_name',
    10000
  );

  const parts = result.stdout.split('---DMI_SEP---');
  const vendor = (parts[0] || '').trim();
  const product = (parts[1] || '').trim();

  if (!vendor || !product) {
    const err = new Error('Could not read DMI vendor/product from host');
    err.statusCode = 502;
    throw err;
  }

  // 2. Derive folder name
  const rawName = `${vendor}_${product}`
    .replace(/\s+/g, '_')
    .replace(/[^a-zA-Z0-9._-]/g, '')
    .substring(0, 80);

  const folderName = sanitizeName(rawName);

  // 3. Resolve and validate path
  const folderPath = await resolveAndValidate(folderName);
  const matchConfPath = path.join(folderPath, 'match.conf');

  // 4. Check if already exists
  try {
    await fs.access(matchConfPath);
    // match.conf exists -- idempotent return
    return { created: false, folder: folderName, vendor, product };
  } catch {
    // Does not exist, create it
  }

  // 5. Create directory
  await fs.mkdir(folderPath, { recursive: true });

  // 6. Write match.conf
  const confContent = [
    '# Auto-generated by LINBO Plugin API',
    `# Hardware: ${vendor} / ${product}`,
    '[match]',
    `vendor = ${vendor}`,
    `product = ${product}`,
    '',
  ].join('\n');

  await fs.writeFile(matchConfPath, confContent, 'utf-8');

  // 7. Broadcast WS event
  ws.broadcast('drivers.profile_created', { folder: folderName, vendor, product });

  return { created: true, folder: folderName, vendor, product };
}

/**
 * List all simple driver profiles (folders with match.conf).
 * Skips legacy folders (those with driver-map.json but no match.conf).
 * @returns {Promise<Array<{ folder: string, vendor: string, product: string, hasDrivers: boolean, fileCount: number, totalSize: number }>>}
 */
async function listProfiles() {
  let entries;
  try {
    entries = await fs.readdir(DRIVERS_BASE, { withFileTypes: true });
  } catch {
    // Base dir does not exist yet
    return [];
  }

  const profiles = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;

    const matchConfPath = path.join(DRIVERS_BASE, entry.name, 'match.conf');
    try {
      const content = await fs.readFile(matchConfPath, 'utf-8');
      const parsed = parseMatchConf(content);

      // Check if dir has files beyond match.conf
      const dirPath = path.join(DRIVERS_BASE, entry.name);
      const files = await fs.readdir(dirPath);
      const hasDrivers = files.some(f => f !== 'match.conf');

      // Count files and total size for the profile
      const fileCount = await countFiles(dirPath);
      const totalSize = await getDirSize(dirPath);

      // Read image assignment
      const image = await readImageConf(entry.name);

      profiles.push({
        folder: entry.name,
        vendor: parsed.vendor,
        product: parsed.products[0] || '',
        hasDrivers,
        fileCount,
        totalSize,
        image,
      });
    } catch {
      // No match.conf -- not a simple driver profile, skip
      continue;
    }
  }

  return profiles;
}

/**
 * Get match.conf data for a specific profile folder.
 * @param {string} folderName - Name of the profile folder
 * @returns {Promise<{ folder: string, vendor: string, products: string[], raw: string }>}
 */
async function getMatchConf(folderName) {
  const safeName = sanitizeName(folderName);
  const matchConfPath = path.join(DRIVERS_BASE, safeName, 'match.conf');

  let content;
  try {
    content = await fs.readFile(matchConfPath, 'utf-8');
  } catch {
    const err = new Error(`match.conf not found in profile "${safeName}"`);
    err.statusCode = 404;
    throw err;
  }

  const parsed = parseMatchConf(content);
  return { folder: safeName, vendor: parsed.vendor, products: parsed.products, raw: content };
}

