/**
 * Read image.conf from a profile folder.
 * Returns the image name string or null if file does not exist.
 * @param {string} folderName - Name of the profile folder
 * @returns {Promise<string|null>}
 */
async function readImageConf(folderName) {
  const imageConfPath = path.join(DRIVERS_BASE, folderName, 'image.conf');
  let content;
  try {
    content = await fs.readFile(imageConfPath, 'utf-8');
  } catch {
    return null;
  }

  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (trimmed === '' || trimmed.startsWith('#')) continue;
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;
    const key = trimmed.substring(0, eqIdx).trim().toLowerCase();
    const value = trimmed.substring(eqIdx + 1).trim();
    if (key === 'image' && value) return value;
  }

  return null;
}

/**
 * List available images (subdirectories of IMAGES_DIR containing .qcow2 files).
 * @returns {Promise<Array<{name: string, filename: string}>>}
 */
async function listAvailableImages() {
  let entries;
  try {
    entries = await fs.readdir(IMAGES_DIR, { withFileTypes: true });
  } catch {
    return [];
  }

  const images = [];
  const skipDirs = new Set(['tmp', 'backups']);

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (skipDirs.has(entry.name)) continue;

    const dirPath = path.join(IMAGES_DIR, entry.name);
    let files;
    try {
      files = await fs.readdir(dirPath);
    } catch {
      continue;
    }

    const qcow2 = files.find(f => f.endsWith('.qcow2'));
    if (qcow2) {
      images.push({ name: entry.name, filename: qcow2 });
    }
  }

  return images;
}

/**
 * Assign a profile to an image. Writes image.conf and regenerates postsync.
 * @param {string} folderName - Profile folder name
 * @param {string} imageName - Image name (directory name in IMAGES_DIR)
 * @returns {Promise<{folder: string, image: string}>}
 */
async function setProfileImage(folderName, imageName) {
  const safeName = sanitizeName(folderName);

  // Validate profile exists (has match.conf)
  const matchConfPath = path.join(DRIVERS_BASE, safeName, 'match.conf');
  try {
    await fs.access(matchConfPath);
  } catch {
    const err = new Error(`Profile not found: ${safeName}`);
    err.statusCode = 404;
    throw err;
  }

  // Validate image name format
  if (!imageName || typeof imageName !== 'string') {
    const err = new Error('Image name must not be empty');
    err.statusCode = 400;
    throw err;
  }
  if (!/^[a-zA-Z0-9][a-zA-Z0-9._-]*$/.test(imageName)) {
    const err = new Error('Image name must start with alphanumeric and contain only [a-zA-Z0-9._-]');
    err.statusCode = 400;
    throw err;
  }

  // Write image.conf
  const imageConfPath = path.join(DRIVERS_BASE, safeName, 'image.conf');
  const confContent = [
    '# Image assignment for driver profile',
    `image = ${imageName}`,
    '',
  ].join('\n');
  await fs.writeFile(imageConfPath, confContent, 'utf-8');

  // Regenerate postsync for this image
  await regeneratePostsync(imageName);

  ws.broadcast('drivers.image_assigned', { folder: safeName, image: imageName });

  return { folder: safeName, image: imageName };
}

/**
 * Remove the image assignment for a profile. Deletes image.conf and regenerates postsync.
 * @param {string} folderName - Profile folder name
 * @returns {Promise<{folder: string, image: null}>}
 */
async function removeProfileImage(folderName) {
  const safeName = sanitizeName(folderName);
  const imageConfPath = path.join(DRIVERS_BASE, safeName, 'image.conf');

  // Read current image name before deleting
  const currentImage = await readImageConf(safeName);

  // Delete image.conf (idempotent)
  try {
    await fs.unlink(imageConfPath);
  } catch {
    // File does not exist â€” return silently
    return { folder: safeName, image: null };
  }

  // Regenerate postsync for the previously assigned image (other profiles may still reference it)
  if (currentImage) {
    await regeneratePostsync(currentImage);
  }

  ws.broadcast('drivers.image_removed', { folder: safeName });

  return { folder: safeName, image: null };
}

/**
 * Regenerate the postsync script for a given image.
 * Scans ALL profile folders to find which ones reference this image.
 * If none reference it, deletes the postsync. Otherwise generates a self-contained script.
 * @param {string} imageName - Image directory name
 * @returns {Promise<void>}
 */
async function regeneratePostsync(imageName) {
  // Find all profiles that reference this image
  let entries;
  try {
    entries = await fs.readdir(DRIVERS_BASE, { withFileTypes: true });
  } catch {
    entries = [];
  }

  const referencingProfiles = [];
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const img = await readImageConf(entry.name);
    if (img === imageName) {
      referencingProfiles.push(entry.name);
    }
  }

  const postsyncPath = path.join(IMAGES_DIR, imageName, imageName + '.treiberpostsync');

  if (referencingProfiles.length === 0) {
    // No profiles reference this image â€” delete postsync if it exists
    try {
      await fs.unlink(postsyncPath);
    } catch {
      // File does not exist â€” nothing to do
    }
    ws.broadcast('drivers.treiberpostsync_updated', { image: imageName, profiles: 0 });
    return;
  }

  // Ensure image directory exists
  const imageDir = path.join(IMAGES_DIR, imageName);
  await fs.mkdir(imageDir, { recursive: true });

  // Generate self-contained postsync script with inline match logic
  const script = `#!/bin/sh
# =============================================================================
# Auto-generated postsync script for image: ${imageName}
# Generated by LINBO Plugin API â€” DO NOT EDIT MANUALLY
# Profiles: ${referencingProfiles.join(', ')}
# =============================================================================

LOG="/tmp/linbo-drivers-postsync.log"
echo "=== LINBO Driver Postsync for ${imageName} $(date) ===" > "$LOG"

# Sync all driver profiles from server
echo "Syncing driver profiles from server..." | tee -a "$LOG"
rsync -a "$LINBOSERVER::drivers/" /cache/linbo-drivers/ 2>&1 | tee -a "$LOG"

# Read client DMI
SYS_VENDOR=$(cat /sys/class/dmi/id/sys_vendor 2>/dev/null | tr -d '\\n\\r' | sed 's/[[:space:]]*$//')
PRODUCT_NAME=$(cat /sys/class/dmi/id/product_name 2>/dev/null | tr -d '\\n\\r' | sed 's/[[:space:]]*$//')
echo "DMI: vendor='$SYS_VENDOR' product='$PRODUCT_NAME'" | tee -a "$LOG"

TARGET="/mnt/Drivers/LINBO"
MATCHED_FOLDERS=""

# Iterate all driver profile folders
for PROFILE_DIR in /cache/linbo-drivers/*/; do
    [ -d "$PROFILE_DIR" ] || continue
    CONF="$PROFILE_DIR/match.conf"
    [ -f "$CONF" ] || continue

    FOLDER=$(basename "$PROFILE_DIR")

    # Parse match.conf â€” extract vendor and product lines
    CONF_VENDOR=""
    CONF_PRODUCTS=""
    IN_MATCH=0
    while IFS= read -r line; do
        case "$line" in
            \\#*|"") continue ;;
            \\[match\\]*) IN_MATCH=1; continue ;;
            \\[*) IN_MATCH=0; continue ;;
        esac
        [ "$IN_MATCH" = "0" ] && continue

        key=$(echo "$line" | sed 's/[[:space:]]*=.*//' | tr -d ' ')
        value=$(echo "$line" | sed 's/[^=]*=[[:space:]]*//')

        case "$key" in
            vendor) CONF_VENDOR="$value" ;;
            product) CONF_PRODUCTS="$CONF_PRODUCTS|$value" ;;
        esac
    done < "$CONF"

    # Wildcard match
    if [ -z "$CONF_VENDOR" ] || [ "$CONF_VENDOR" = "*" ]; then
        echo "  $FOLDER: default/wildcard profile, matched" | tee -a "$LOG"
        MATCHED_FOLDERS="$MATCHED_FOLDERS $FOLDER"
        continue
    fi

    # Vendor must match exactly
    if [ "$CONF_VENDOR" != "$SYS_VENDOR" ]; then
        continue
    fi

    # Product substring match
    if [ -z "$CONF_PRODUCTS" ] || [ "$CONF_PRODUCTS" = "|*" ]; then
        echo "  $FOLDER: vendor matched, wildcard product" | tee -a "$LOG"
        MATCHED_FOLDERS="$MATCHED_FOLDERS $FOLDER"
        continue
    fi

    OLD_IFS="$IFS"
    IFS="|"
    for pat in $CONF_PRODUCTS; do
        [ -z "$pat" ] && continue
        case "$PRODUCT_NAME" in
            *"$pat"*)
                echo "  $FOLDER: matched (product contains '$pat')" | tee -a "$LOG"
                MATCHED_FOLDERS="$MATCHED_FOLDERS $FOLDER"
                break
                ;;
        esac
    done
    IFS="$OLD_IFS"
done

echo "Matched folders: $MATCHED_FOLDERS" | tee -a "$LOG"

# Copy matched driver folders to Windows target
for FOLDER in $MATCHED_FOLDERS; do
    SRC="/cache/linbo-drivers/$FOLDER"
    if [ -d "$SRC" ]; then
        FILE_COUNT=$(find "$SRC" -type f ! -name "match.conf" ! -name "image.conf" 2>/dev/null | wc -l)
        if [ "$FILE_COUNT" -gt 0 ]; then
            echo "  Copying drivers from: $FOLDER ($FILE_COUNT files)" | tee -a "$LOG"
            mkdir -p "$TARGET/$FOLDER"
            find "$SRC" -type f ! -name "match.conf" ! -name "image.conf" -exec cp -a {} "$TARGET/$FOLDER/" \\; 2>/dev/null
        else
            echo "  $FOLDER: no driver files, skipping copy" | tee -a "$LOG"
        fi
    fi
done

# pnputil setup if INF files were copied
INF_COUNT=$(find "$TARGET" -iname '*.inf' 2>/dev/null | wc -l)
if [ "$INF_COUNT" -gt 0 ]; then
    printf '@echo off\\r\\n' > "$TARGET/pnputil-install.cmd"
    printf 'pnputil /add-driver C:\\\\Drivers\\\\LINBO\\\\*.inf /subdirs /install\\r\\n' >> "$TARGET/pnputil-install.cmd"
    printf 'del "%%~f0"\\r\\n' >> "$TARGET/pnputil-install.cmd"
    printf 'exit /b 0\\r\\n' >> "$TARGET/pnputil-install.cmd"

    cat > /tmp/linbo-driver-install.reg << 'REG'
Windows Registry Editor Version 5.00

[HKEY_LOCAL_MACHINE\\SOFTWARE\\Microsoft\\Windows\\CurrentVersion\\RunOnce]
"LinboDriverInstall"="C:\\\\Drivers\\\\LINBO\\\\pnputil-install.cmd"
REG
    linbo_patch_registry /tmp/linbo-driver-install.reg 2>&1 | tee -a "$LOG"
    rm -f /tmp/linbo-driver-install.reg
    echo "Driver auto-install: $INF_COUNT INF files, RunOnce set" | tee -a "$LOG"
else
    echo "No INF files found â€” skipping pnputil setup" | tee -a "$LOG"
fi

cp "$LOG" "/cache/linbo-drivers-postsync.log" 2>/dev/null
`;

  await fs.writeFile(postsyncPath, script, { mode: 0o755 });

  ws.broadcast('drivers.treiberpostsync_updated', { image: imageName, profiles: referencingProfiles.length });
}

