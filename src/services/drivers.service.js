/**
 * List all files in a driver profile folder.
 * Excludes match.conf from the listing (it's metadata, not a driver file).
 * @param {string} folderName - Name of the profile folder
 * @returns {Promise<Array<{name: string, path: string, size: number, isDirectory: boolean}>>}
 */
async function listFiles(folderName) {
  const safeName = sanitizeName(folderName);
  const folderPath = await resolveAndValidate(safeName);

  let allFiles;
  try {
    allFiles = await listDirRecursive(folderPath, '');
  } catch (err) {
    if (err.code === 'ENOENT') {
      const error = new Error(`Profile not found: ${safeName}`);
      error.statusCode = 404;
      throw error;
    }
    throw err;
  }

  // Exclude match.conf from the listing
  return allFiles.filter(f => f.path !== 'match.conf');
}

/**
 * Upload a file into a driver profile folder.
 * @param {string} folderName - Name of the profile folder
 * @param {string} relPath - Relative path within the folder
 * @param {Buffer} buffer - File content
 * @returns {Promise<{path: string, size: number}>}
 */
async function uploadFile(folderName, relPath, buffer) {
  const safeName = sanitizeName(folderName);
  const safeRelPath = sanitizeRelativePath(relPath);
  const filePath = await resolveAndValidate(safeName, safeRelPath);

  // Ensure parent directory exists
  await fs.mkdir(path.dirname(filePath), { recursive: true });
  await fs.writeFile(filePath, buffer, { mode: 0o644 });

  const stat = await fs.stat(filePath);

  ws.broadcast('drivers.file_uploaded', { folder: safeName, path: safeRelPath, size: stat.size });

  return { path: safeRelPath, size: stat.size };
}

/**
 * Delete a file from a driver profile folder.
 * @param {string} folderName - Name of the profile folder
 * @param {string} relPath - Relative path of the file to delete
 * @returns {Promise<{deleted: string}>}
 */
async function deleteFile(folderName, relPath) {
  const safeName = sanitizeName(folderName);
  const safeRelPath = sanitizeRelativePath(relPath);
  const filePath = await resolveAndValidate(safeName, safeRelPath);

  try {
    await fs.unlink(filePath);
  } catch (unlinkErr) {
    if (unlinkErr.code === 'ENOENT') {
      const err = new Error(`File not found: ${safeRelPath}`);
      err.statusCode = 404;
      throw err;
    }
    throw unlinkErr;
  }

  ws.broadcast('drivers.file_deleted', { folder: safeName, path: safeRelPath });

  return { deleted: safeRelPath };
}

/**
 * Delete an entire driver profile folder.
 * @param {string} folderName - Name of the profile folder
 * @returns {Promise<{deleted: string}>}
 */
async function deleteProfile(folderName) {
  const safeName = sanitizeName(folderName);
  const dirPath = await resolveAndValidate(safeName);

  // Use readdir instead of access to avoid TOCTOU race
  try {
    await fs.readdir(dirPath);
  } catch (readErr) {
    if (readErr.code === 'ENOENT') {
      const err = new Error(`Profile not found: ${safeName}`);
      err.statusCode = 404;
      throw err;
    }
    throw readErr;
  }

  await fs.rm(dirPath, { recursive: true, force: true });

  ws.broadcast('drivers.profile_deleted', { folder: safeName });

  return { deleted: safeName };
}

/**
 * Update match.conf content for a driver profile.
 * @param {string} folderName - Name of the profile folder
 * @param {string} content - New match.conf content
 * @returns {Promise<{folder: string, vendor: string, products: string[]}>}
 */
async function updateMatchConf(folderName, content) {
  const safeName = sanitizeName(folderName);

  if (typeof content !== 'string') {
    const err = new Error('Content must be a string');
    err.statusCode = 400;
    throw err;
  }

  if (content.length > 10 * 1024) {
    const err = new Error('Content exceeds maximum size (10KB)');
    err.statusCode = 400;
    throw err;
  }

  // Validate match values before writing (prevents shell injection in postsync scripts)
  const { validateMatchValue } = require('../lib/driver-shell');
  const parsed = parseMatchConf(content);

  const vendorErr = validateMatchValue(parsed.vendor);
  if (vendorErr) {
    const err = new Error(`Invalid vendor in match.conf: ${vendorErr}`);
    err.statusCode = 400;
    throw err;
  }
  for (const product of parsed.products) {
    const productErr = validateMatchValue(product);
    if (productErr) {
      const err = new Error(`Invalid product in match.conf: ${productErr}`);
      err.statusCode = 400;
      throw err;
    }
  }

  const matchConfPath = await resolveAndValidate(safeName, 'match.conf');

  // Atomic write with random suffix (PID-reuse safe) + cleanup on failure
  const suffix = Date.now() + '-' + Math.random().toString(36).slice(2);
  const tmp = matchConfPath + '.tmp.' + suffix;
  try {
    await fs.writeFile(tmp, content, { mode: 0o644 });
    await fs.rename(tmp, matchConfPath);
  } catch (writeErr) {
    await fs.unlink(tmp).catch(() => {});
    throw writeErr;
  }

  ws.broadcast('drivers.match_conf_updated', { folder: safeName, vendor: parsed.vendor, products: parsed.products });

  return { folder: safeName, vendor: parsed.vendor, products: parsed.products };
}

/**
 * Extract an archive (ZIP, EXE, 7z) into a driver profile folder.
 * @param {string} folderName - Name of the profile folder
 * @param {string} archivePath - Path to the uploaded archive on disk
 * @param {string} originalName - Original filename (for format detection)
 * @returns {Promise<{entryCount: number, totalUncompressed: number}>}
 */
async function extractArchive(folderName, archivePath, originalName) {
  const safeName = sanitizeName(folderName);
  const profileDir = await resolveAndValidate(safeName);

  // Verify profile directory exists
  try {
    await fs.access(profileDir);
  } catch {
    const err = new Error(`Profile not found: ${safeName}`);
    err.statusCode = 404;
    throw err;
  }

  // Concurrency lock â€” prevent parallel extractions to same profile
  const lockFile = path.join(profileDir, '.extracting');
  try {
    await fs.writeFile(lockFile, `${process.pid}\n${new Date().toISOString()}`, { flag: 'wx' });
  } catch (lockErr) {
    if (lockErr.code === 'EEXIST') {
      const err = new Error(`Extraction already in progress for profile: ${safeName}`);
      err.statusCode = 409;
      throw err;
    }
    throw lockErr;
  }

  try {
    return await _doExtract(safeName, profileDir, archivePath, originalName);
  } finally {
    await fs.unlink(lockFile).catch(() => {});
  }
}

async function _doExtract(safeName, profileDir, archivePath, originalName) {

  const { execFile } = require('child_process');

  function execFileAsync(cmd, args, opts = {}) {
    return new Promise((resolve, reject) => {
      execFile(cmd, args, opts, (err, stdout, stderr) => {
        if (err) { err.stdout = stdout; err.stderr = stderr; reject(err); }
        else resolve({ stdout, stderr });
      });
    });
  }

  // Detect archive format from original filename
  const ext = (originalName || '').toLowerCase().replace(/^.*\./, '.');
  const use7z = ext === '.exe' || ext === '.7z';

  let entryCount = 0;
  let totalUncompressed = 0;

  if (use7z) {
    // ---- 7z path (for .exe / .7z) ----
    let listOutput;
    try {
      const result = await execFileAsync('7z', ['l', archivePath], { maxBuffer: 10 * 1024 * 1024 });
      listOutput = result.stdout;
    } catch (err) {
      throw Object.assign(new Error('Invalid archive: ' + (err.stderr || err.message)), { statusCode: 400 });
    }

    const lines = listOutput.split('\n');
    let inEntries = false;
    for (const line of lines) {
      if (line.startsWith('---')) {
        inEntries = !inEntries;
        continue;
      }
      if (!inEntries) continue;

      const m = line.match(/^\S+\s+\S+\s+\S+\s+(\d+)\s+\d+\s+(.+)$/);
      if (!m) continue;

      const size = parseInt(m[1], 10);
      const entryName = m[2].trim();
      if (!entryName) continue;
      entryCount++;

      if (entryName.includes('..')) {
        throw Object.assign(new Error(`Archive contains path traversal: ${entryName}`), { statusCode: 400 });
      }
      if (entryName.startsWith('/')) {
        throw Object.assign(new Error(`Archive contains absolute path: ${entryName}`), { statusCode: 400 });
      }

      totalUncompressed += size;
    }

    if (entryCount > MAX_ZIP_ENTRIES) {
      throw Object.assign(
        new Error(`Archive contains too many entries: ${entryCount} (max ${MAX_ZIP_ENTRIES})`),
        { statusCode: 400 }
      );
    }

    if (totalUncompressed > MAX_ZIP_SIZE) {
      throw Object.assign(
        new Error(`Archive uncompressed size too large: ${Math.round(totalUncompressed / 1024 / 1024)}MB (max ${Math.round(MAX_ZIP_SIZE / 1024 / 1024)}MB)`),
        { statusCode: 400 }
      );
    }

    // Extract to temp dir first, then validate paths and move (defense-in-depth)
    const tmpDir = await fs.mkdtemp(path.join(os.tmpdir(), 'linbo-extract-'));
    try {
      await execFileAsync('7z', ['x', `-o${tmpDir}`, '-aoa', archivePath], {
        maxBuffer: 10 * 1024 * 1024,
        timeout: 600000, // 10 min
      });
      // Validate: no extracted file escapes tmpDir, remove symlinks before move
      await validateExtractedPaths(tmpDir);
      await removeSymlinks(tmpDir);
      // Move contents to profileDir
      await moveContents(tmpDir, profileDir);
    } catch (err) {
      if (err.statusCode) throw err;
      throw Object.assign(
        new Error('Archive extraction failed: ' + (err.stderr || err.message)),
        { statusCode: 500 }
      );
    } finally {
      await fs.rm(tmpDir, { recursive: true, force: true }).catch(() => {});
    }

    // Inno Setup .exe detection
    if (ext === '.exe') {
      const extracted = await fs.readdir(profileDir);
      const peSections = ['.text', '.rdata', '.rsrc', '.data', '.idata', '.edata', '.bss', '.tls', '.itext', '.didata', 'CERTIFICATE'];
      const nonMetaFiles = extracted.filter(f => f !== 'match.conf');
      const allPE = nonMetaFiles.every(f => peSections.includes(f) || f === '[0]');
      if (allPE && nonMetaFiles.length > 0) {
        for (const f of nonMetaFiles) {
          await fs.rm(path.join(profileDir, f), { recursive: true, force: true });
        }
        try {
          await execFileAsync('innoextract', ['-e', '-d', profileDir, archivePath], {
            maxBuffer: 50 * 1024 * 1024,
            timeout: 600000,
          });
          const topEntries = await fs.readdir(profileDir);
          const nonMeta = topEntries.filter(f => f !== 'match.conf');
          if (nonMeta.length === 1) {
            const singleDir = path.join(profileDir, nonMeta[0]);
            try {
              const st = await fs.stat(singleDir);
              if (st.isDirectory()) {
                const innerFiles = await fs.readdir(singleDir);
                for (const f of innerFiles) {
                  await fs.rename(path.join(singleDir, f), path.join(profileDir, f));
                }
                await fs.rm(singleDir, { recursive: true, force: true });
              }
            } catch { /* not a directory */ }
          }
          entryCount = await countFiles(profileDir);
        } catch (innoErr) {
          throw Object.assign(
            new Error('Inno Setup extraction failed: ' + (innoErr.stderr || innoErr.message)),
            { statusCode: 500 }
          );
        }
      }
    }
  } else {
    // ---- unzip path (for .zip and fallback) ----
    let listOutput;
    try {
      const result = await execFileAsync('unzip', ['-l', archivePath], { maxBuffer: 10 * 1024 * 1024 });
      listOutput = result.stdout;
    } catch (err) {
      throw Object.assign(new Error('Invalid ZIP file: ' + (err.stderr || err.message)), { statusCode: 400 });
    }

    const lines = listOutput.split('\n');
    for (const line of lines) {
      const match = line.match(/^\s*(\d+)\s+\d{2}-\d{2}-\d{2,4}\s+\d{2}:\d{2}\s+(.+)$/);
      if (!match) continue;

      const size = parseInt(match[1], 10);
      const entryName = match[2];
      entryCount++;

      if (entryName.includes('..')) {
        throw Object.assign(new Error(`ZIP contains path traversal: ${entryName}`), { statusCode: 400 });
      }
      if (entryName.startsWith('/')) {
        throw Object.assign(new Error(`ZIP contains absolute path: ${entryName}`), { statusCode: 400 });
      }

      totalUncompressed += size;
    }

    if (entryCount > MAX_ZIP_ENTRIES) {
      throw Object.assign(
        new Error(`ZIP contains too many entries: ${entryCount} (max ${MAX_ZIP_ENTRIES})`),
        { statusCode: 400 }
      );
    }

    if (totalUncompressed > MAX_ZIP_SIZE) {
      throw Object.assign(
        new Error(`ZIP uncompressed size too large: ${Math.round(totalUncompressed / 1024 / 1024)}MB (max ${Math.round(MAX_ZIP_SIZE / 1024 / 1024)}MB)`),
        { statusCode: 400 }
      );
    }

    // Extract to temp dir first, then validate paths and move (defense-in-depth)
    const tmpDir = await fs.mkdtemp(path.join(os.tmpdir(), 'linbo-extract-'));
    try {
      await execFileAsync('unzip', ['-o', '-K', archivePath, '-d', tmpDir], {
        maxBuffer: 10 * 1024 * 1024,
        timeout: 600000, // 10 min
      });
      // Validate: no extracted file escapes tmpDir, remove symlinks before move
      await validateExtractedPaths(tmpDir);
      await removeSymlinks(tmpDir);
      // Move contents to profileDir
      await moveContents(tmpDir, profileDir);
    } catch (err) {
      if (err.statusCode) throw err;
      throw Object.assign(
        new Error('ZIP extraction failed: ' + (err.stderr || err.message)),
        { statusCode: 500 }
      );
    } finally {
      await fs.rm(tmpDir, { recursive: true, force: true }).catch(() => {});
    }
  }

  // Post-extraction: remove any symlinks
  await removeSymlinks(profileDir);

  ws.broadcast('drivers.archive_extracted', { folder: safeName, entryCount, totalUncompressed });

  return { entryCount, totalUncompressed };
}

