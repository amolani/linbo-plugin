/**
 * Parse an INI-style match.conf content.
 * Looks for [match] section, extracts vendor and product lines.
 * @param {string} content - Raw match.conf content
 * @returns {{ vendor: string, products: string[] }}
 */
function parseMatchConf(content) {
  const lines = content.split('\n');
  let inMatch = false;
  let vendor = '';
  const products = [];

  for (const line of lines) {
    const trimmed = line.trim();

    // Skip comments and empty lines
    if (trimmed === '' || trimmed.startsWith('#')) continue;

    // Section detection
    if (trimmed.startsWith('[')) {
      inMatch = trimmed.toLowerCase() === '[match]';
      continue;
    }

    if (!inMatch) continue;

    // Parse key = value
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;

    const key = trimmed.substring(0, eqIdx).trim().toLowerCase();
    const value = trimmed.substring(eqIdx + 1).trim();

    if (key === 'vendor') {
      vendor = value;
    } else if (key === 'product') {
      products.push(value);
    }
  }

  return { vendor, products };
}

